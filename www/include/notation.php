                                  <p>Wallich compiled and printed his <em>Catalogue</em>, a few pages at a time, during
                                      a five- and a two-year period spanning over two decades. Although the basis of his
                                      arrangement was established at the start, Wallich was not always consistent and
                                      introduced duplications and ambiguities throughout the Catalogue. He had set out
                                      to number and name <em>Entries</em>, with associated numbered
                                      <em>Collections</em>. Entries would consist of the taxon name and related
                                      information, and Collections would specify details such as the names of
                                      collectors, localities and dates. But in the process, when Wallich listed only one
                                      collection for a species, he often only numbered the Entry and neglected to number
                                      the Collection separately. We assume this was because Wallich intended the
                                      Catalogue pages to serve as labels for herbarium specimens, and not as a permanent
                                      reference. However, such inconcistencies&nbsp; became a bigger issue later on
                                      during the distribution, when Wallich&#39;s system became much more intricate. For
                                      example, many new Collections were introduced as <em>Addenda</em> to be listed
                                      under Entries that had already been printed with single collections, which led to
                                      obscurities and complicated the interpretation of Wallich&#39;s numbering.
                                  </p>
                                  <p>In order to unambiguously refer to elements within the Wallich Catalogue, it has
                                      been necessary to clarify these occurrences and establish an unambiguous numbering
                                      convention for Collections and Addenda in the Wallich Catalogue<em>. </em>We refer
                                      to this convention as as the &lsquo;Edinburgh Notation&rsquo;. The Edinburgh
                                      Notation has been devised by Mark Watson and Heleen Plaisier at Royal Botanic
                                      Garden Edinburgh, and was refined through the preparation of the <em>Wallich
                                          Catalogue Online</em>. Square brackets, &lsquo;[&rsquo; and &lsquo;]&rsquo;,
                                      are used to mark any data that has been added to Wallich&rsquo;s original.</p>
