                                 </div><em>Wallich Catalogue Online</em> is led by Mark Watson and Heleen Plaisier at
                                 Royal Botanic Garden, Edinburgh. The project began in 2008 with the digital photography
                                 and image processing of one of RBGE’s copies of the <em>Wallich Catalogue</em> (thought
                                 to be Sir William Hooker’s) by Bhaskar Adhikari and Mark. Mark then worked with
                                 Kristance Harlow to create the first digital index to the nearly 9,150 entries by
                                 marking up the digital images, adding numbers and plant names to the entries using the
                                 <em>Wallich Cruncher</em> software developed by Roger Hyam.</p>
                                 <p>Heleen undertook the next stage in 2012 by placing digital markers on all
                                     collection-level elements and coding them with the <em>Edinburgh Notation,</em>
                                     which she developed with Mark. This process showed just how idiosyncratic and
                                     error-strewn Wallich’s numbering system was, and refining the notation took many
                                     months.</p>
                                 <p>This was as far as the <em>Wallich Cruncher</em> software would go, and Roger
                                     developed its replacement using Drupal – the same software that is currently used
                                     to display the <em>Wallich Catalogue Online</em>. Roger created image fragments
                                     using the entry and collection-level mark-up, and arranged for all the different
                                     fragments that related to one single entry to be displayed together. For the first
                                     time users could easily see Wallich’s many additions, annotations and corrections
                                     alongside the original entry. The result was a useful online resource that people
                                     were already starting to value.</p>
                                 <p>However, specific collection information still needed to be entered into the system
                                     for it to fulfil its potential. This was a large task – there were thought to be
                                     ca. 20,000 collections – and one that was not at all straightforward. Wallich’s
                                     handwriting is difficult to read, and he frequently uses non-standard
                                     abbreviations, peculiar spellings and latinised forms. The opportunity to move
                                     forward with this came with the digitisation of the 'Wallich Herbarium' at Royal
                                     Botanic Gardens, Kew, in 2014, because the information in the <em>Wallich Catalogue
                                         Online</em> provides important data for the specimens.</p>
                                 <p>RBGs Kew and Edinburgh joined hands, with Kew’s team led by Jonathan Krieger and Tim
                                     Utteridge. Since the IDC glossary and gazetteer (Smith &amp; Parson, 1971) was
                                     known to contain errors, Heleen created new standardized lists of the 41
                                     collectors, 570 localities, 13 herbarium and 22 cultivated sources that were cited
                                     in the Wallich Catalogue. Heleen also produced ‘helper files’ to explain Wallich’s
                                     handwriting, abbreviations and Latin phrases, which Helen Hartley and Katherine
                                     Harrington used when they transcribed the collection information. Heleen’s lists
                                     and helper files have since been incorporated into the <em>Wallich Catalogue
                                         Online.</em></p>
