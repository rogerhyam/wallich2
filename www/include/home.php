                <p>
                    The <em>Wallich Catalogue Online</em> is an interactive tool to help people use
                    the <em>Wallich Catalogue* </em>and interpret the herbarium specimens Nathaniel Wallich
                    distributed on behalf of the British East India Company between 1829 and 1847. The 306
                    lithographically printed pages list 9,148 &#39;Entries&#39;, usually species, and give
                    collecting details of 20,897 &#39;Collections&#39;. The pages were originally intended
                    to be cut up and used as specimen labels and, as the specimens are the basis of
                    thousands of plant names described from S and SE Asia, understanding these is important
                    in the typification and application of thousands of plant names in many Asian countries.
                </p>
                <p>The numerous corrections and additions which occur sporadically through the
                    <em>Catalogue </em>are brought together and viewed under each Entry number.
                    Wallich&#39;s mistakes and inconsistencies are explained, and the Collections
                    re-numbered using the &#39;Edinburgh Notation&#39; which enables them to be cited
                    unambiguously.
                </p>
                <p>The <em>Wallich Catalogue Online</em> is regularly updated and the data given may
                    still be provisional and awaiting confirmation - so this should be used as a guide
                    and not as a static reference. People are encouraged to provide feedback to help
                    improve the information for the benefit of future users.</p>
                <p>Use the search box (above right) to find a page, Entry number or taxon name, or use
                    the &#39;Catalogue Pages&#39; and &#39;Catalogue Entries&#39; tabs to browse the
                    <em>Wallich Catalogue</em>.
                </p>
