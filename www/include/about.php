                                 </div>Digital photography of the specimens in the East India Company Herbarium (EICH,
                                 also known as the 'Wallich Herbaruim' with the acronym K-W) commenced with a pilot
                                 project at Kew in 2014, and imaging of the whole herbarium was completed in August
                                 2015. All the specimens in EICH were added to Kew’s online database HerbCat (<a
                                     href="http://apps.kew.org/herbcat/navigator.do">http://apps.kew.org/herbcat/navigator.do</a>)
                                 where the specimens can be found by searching for the project tag "East India Company
                                 Herbarium”.</p>
                                 <p>The final stage before the public release in October 2015 was undertaken by
                                     Jonathan, Heleen and Mark. This consisted of incorporating links between herbarium
                                     specimen images at Kew and relevant collections in the <em>Wallich Catalogue</em>,
                                     as well as extensive quality control and data integrity checking. Roger continued
                                     to develop the software and data outputs that were needed for these steps, and he
                                     implemented the public user interface which incorporated designs by Caroline Muir.
                                 </p>
                                 <p>Heleen, Roger and Mark continue to maintain the <em>Wallich Catalogue Online</em>,
                                     correcting mistakes and looking to link to other online herbaria which have
                                     specimens from theh Wallichian Distribution.</p>
                                 <p>Those involved in the project would like to recognise the institutional support of
                                     RBG Edinburgh and RBG Kew, and generous financial assistance from The Andrew W.
                                     Mellon Foundation, BHL-Europe, Sibbald Trust and Friends of RBG Edinburgh.</p>
